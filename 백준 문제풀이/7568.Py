# 7568 번 문제 

# 문제요약 
'''
한사람당 몸무게, 키 값이 주어지는데, 
다른사람과 비교했을 때 몸무게도 크고, 키도 크다면 
그 사람이 다른사람보다 덩치가 크다고 표현한다. 
여러명의 사람의 몸무게,키 값을 통해 덩치 등수를 출력하는 문제 
'''

# 문제 풀이 방식 
'''
모든 사람의 몸무게와 키를 비교해야하므로 
2차원 배열을 만들어, 하나씩 비교하여 등수를 부여한다. 
이 때, 몸무게와 키 모두 상대방보다 크다는 조건에 부합하지 않는다는 조건식을 통해 같은 등수를 부여한다.
먼저 모든 사람에게 1등을 부여한뒤 상대방과 비교했을 때 덩치가 작다면 
등수를 +1 시키는 방식을 생각중 
-> 몸무게가 작은데, 키가 같다면 덩치등수를 +1, 몸무게는 같은데, 키가 작다면 덩치등수 +1 하는 조건도 넣었는데 해당 문제에서 같은 것에 대한 
조건이 없어기에 이 조건들을 빼니까 정답이 되었다. 
'''
import sys

N = int(input())
p = []
# 덩치 등수 부여 
grade = 1

for _ in range(N) : 
  person = list(map(int,sys.stdin.readline().split()))
  person.append(grade) # 기본 덩치 부여 
  p.append(person)

# 몸무게 키 비교 
for i in range(len(p)) :  
  for j in range(len(p)) :
    if i != j :  # 자기자신을 제외하고 비교해야 하므로 넣은 조건식
      if p[i][0] < p[j][0] : # 몸무게가 작을 경우
        if p[i][1] < p[j][1]:  # 키가 작을 경우  
          p[i][2] += 1              # 덩치 등수 +1 

# 등수 출력
for i in range(len(p)) : 
  print(p[i][2], end=' ')

# 다른 사람 풀이를 보니 
# 나처럼 if 문안에 또 if 문을 넣는 형식이 아닌 and 조건으로 푼 형식이 많았다 
if p[i][0] < p[j][0] and p[i][1] < p[j][1]: # 몸무게가 작을 경우
  p[i][2] += 1              # 덩치 등수 +1 

# 이런 형식으로 한번에 조건식을 거는 방식을 생각못했다. 
# 그렇다고 내가 푼 방식이 시간이 더 오래 걸리는 풀이방식은 아니었다.


    
